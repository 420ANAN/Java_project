public class Addition {
    public static void main(String[] args) {
        int num1 = 5; // First number
        int num2 = 10; // Second number

        // Calculate the sum
        int sum = num1 + num2;

        // Print the result
        System.out.println("The sum of " + num1 + " and " + num2 + " is: " + sum);
    }
}
