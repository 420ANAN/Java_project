public class ComparisonExample {
    public static void main(String[] args) {
        int x = 10;
        int y = 20;

        // Equal to
        System.out.println("x == y: " + (x == y)); // false

        // Not equal to
        System.out.println("x != y: " + (x != y)); // true

        // Greater than
        System.out.println("x > y: " + (x > y)); // false

        // Less than
        System.out.println("x < y: " + (x < y)); // true

        // Greater than or equal to
        System.out.println("x >= y: " + (x >= y)); // false

        // Less than or equal to
        System.out.println("x <= y: " + (x <= y)); // true
    }
}
