public class RelationalOperatorExample {
    public static void main(String[] args) {
        int a = 5;
        int b = 10;

        // Equal to
        System.out.println("a == b: " + (a == b)); // false

        // Not equal to
        System.out.println("a != b: " + (a != b)); // true

        // Greater than
        System.out.println("a > b: " + (a > b)); // false

        // Less than
        System.out.println("a < b: " + (a < b)); // true

        // Greater than or equal to
        System.out.println("a >= b: " + (a >= b)); // false

        // Less than or equal to
        System.out.println("a <= b: " + (a <= b)); // true
    }
}
